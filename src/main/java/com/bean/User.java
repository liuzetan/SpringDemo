package com.bean;

public class User {
    private int user_id;
    private String name;
    private String age;
    private String lengthOfDick;
    private String sex;

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getLengthOfDick() {
        return lengthOfDick;
    }

    public void setLengthOfDick(String lengthOfDick) {
        this.lengthOfDick = lengthOfDick;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
